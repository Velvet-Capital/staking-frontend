const Web3Context = createContext<Web3ContextType>({
  account: null,
  connect: async () => {},
  disconnect: () => {},
  isConnected: false,
  provider: null,
  veVirtualContract: new ethers.Contract(
    process.env.REACT_APP_VEVIRTUAL_ADDRESS || '',
    veVirtualAbi,
    new ethers.providers.JsonRpcProvider(process.env.REACT_APP_RPC_URL)
  ),
  mockTokenContract: new ethers.Contract(
    process.env.REACT_APP_MOCK_TOKEN_ADDRESS || '',
    MockTokenAbi,
    new ethers.providers.JsonRpcProvider(process.env.REACT_APP_RPC_URL)
  ),
});

export const useWeb3 = () => useContext(Web3Context);

interface Web3ProviderProps {
  children: ReactNode;
}

export const Web3Provider: React.FC<Web3ProviderProps> = ({ children }) => {
  const [account, setAccount] = useState<string | null>(null);
  const [provider, setProvider] = useState<ethers.providers.Web3Provider | null>(null);
  const [veVirtualContract, setVeVirtualContract] = useState<Contract | null>(null);
  const [mockTokenContract, setMockTokenContract] = useState<Contract | null>(null);

  const connect = async () => {
    if (typeof window.ethereum === 'undefined') {
      console.error('Please install MetaMask!');
      return;
    }

    try {
      // Request account access
      const accounts = await window.ethereum.request({ method: 'eth_requestAccounts' });
      const account = accounts[0];
      
      // Create provider and signer
      const provider = new ethers.providers.Web3Provider(window.ethereum);
      const signer = provider.getSigner();
      setProvider(provider);
      setAccount(account);

      // Initialize contracts
      const veVirtualAddress = process.env.REACT_APP_VEVIRTUAL_ADDRESS;
      const mockTokenAddress = process.env.REACT_APP_MOCK_TOKEN_ADDRESS;

      console.log('Contract Addresses:', {
        veVirtualAddress,
        mockTokenAddress,
        hasProvider: !!provider,
        hasSigner: !!signer
      });

      if (veVirtualAddress) {
        const veVirtualContract = new ethers.Contract(
          veVirtualAddress,
          veVirtualAbi,
          signer
        );
        setVeVirtualContract(veVirtualContract);
        console.log('VeVirtual contract initialized:', veVirtualContract);
      } else {
        console.error('VeVirtual address not found in environment variables');
      }

      if (mockTokenAddress) {
        const mockTokenContract = new ethers.Contract(
          mockTokenAddress,
          MockTokenAbi,
          signer
        );
        setMockTokenContract(mockTokenContract);
        console.log('Mock token contract initialized:', mockTokenContract);
      } else {
        console.error('Mock token address not found in environment variables');
      }
    } catch (error) {
      console.error('Error connecting to wallet:', error);
    }
  };

  const disconnect = () => {
    setAccount(null);
    setProvider(null);
    setVeVirtualContract(null);
    setMockTokenContract(null);
  };

  useEffect(() => {
    if (typeof window.ethereum !== 'undefined') {
      window.ethereum.on('accountsChanged', (accounts: string[]) => {
        if (accounts.length === 0) {
          disconnect();
        } else {
          setAccount(accounts[0]);
        }
      });

      window.ethereum.on('chainChanged', () => {
        window.location.reload();
      });
    }

    return () => {
      if (typeof window.ethereum !== 'undefined') {
        window.ethereum.removeAllListeners('accountsChanged');
        window.ethereum.removeAllListeners('chainChanged');
      }
    };
  }, []);

  return (
    <Web3Context.Provider
      value={{
        account,
        connect,
        disconnect,
        isConnected: !!account,
        provider,
        veVirtualContract,
        mockTokenContract,
      }}
    >
      {children}
    </Web3Context.Provider>
  );
}; 